CREATE DATABASE IF NOT EXISTS Utils;
USE Utils;

-- -----------------------------------------------------
-- table for Newsletter with Required columns
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS Utils.Newsletter(
 ID INT NOT NULL AUTO_INCREMENT,
 Email VARCHAR(255) NOT NULL,
 Datestamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
 Modified_Datastamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
 PRIMARY KEY (id));


CREATE TABLE IF NOT EXISTS Utils.markers(
  id INT NOT NULL AUTO_INCREMENT,
  regions varchar (50) NOT NULL,
    name varchar (50) NOT NULL,
  value int (11) NOT NULL DEFAULT '0',
  date_changed timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON
UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY
(id));

