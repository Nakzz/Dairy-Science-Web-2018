    <h4>Overview</h4>
    <p>The Specific Aim of this proposal is to systematically evaluate the profitability of dairy reproductive management strategies and develop a suite of simulation models and decision support systems that will allow scientists, farmers, farm consultants, and extension agents assess the economic value of reproductive programs. One thrust of my programs is to understand, simulate, and optimize dairy reproductive profitable management strategies.</p>
    <h4>Objectives</h4>
    <ol>
      <li>Therefore, Objective 1 is to identify, synthesize, and organize scientific knowledge and technologies that would improve dairy reproductive efficiency and herd economics.</li>
      <li>Then Objective 2, supported in previous Objective, is to develop a suite of innovative stochastic, dynamic dairy herd simulation and optimization models capable to integrate reproductive managerial changes and assess the economic value of these changes.</li>
      <li>And Objective 3, supported in previous two objectives, is to develop and disseminate a suite of web-based decision computer applications to support final users cost-efficient and profitable dairy reproductive decision-making. </li>
    </ol>  
    <h4>Scope</h4>
    <p>Research results will generate:</p>
    <ol>
      <li>methodological frameworks to evaluate the economics of reproductive programs, and</li>
      <li>reproductive management strategies applicable on most Wisconsin farming systems,</li>
    </ol>
    <p>whereas extension results will generate:</p>
    <ol>
      <li>web-based decision support tools and</li>
      <li>Wisconsin improved reproductive efficiency because of adoption and application of practical tools.</li>
    </ol>
