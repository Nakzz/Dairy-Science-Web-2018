    <h4>Overview</h4>
    <p>The Specific Aim of this proposal is to promote more sustainable Wisconsin dairy farm production systems applying systematic whole-farm integrated evaluations. Proposed work will adjust, adapt, and incorporate the Integrated Farm System Model (IFSM), the most cited and the state-of-the-art model, to Wisconsin dairy farm conditions. Research results will generate integrated methodological frameworks to better assess dairy farm long-term sustainability. Extension outcomes will include messages of best management practices associated with online user-friendly decision support tools and measurable impacts of improved Wisconsin dairy farm sustainability.</p>
    <h4>Objectives</h4>
    <ol>
      <li>Objective 1 is to understand and apply the latest version of the IFSM to Wisconsin dairy farm conditions.</li>
      <li>Then Objective 2, supported in previous Objective, is to integrate the IFSM with: a) data already collected from Wisconsin dairy farms, b) additional dairy farm data to be collected, c) data from soil and weather synthetized from official sources, and d) existing Wisconsin decision support tools.</li>
      <li>Objective 3, supported in previous two Objectives, is to perform a high level, whole dairy farm, integrated assessment of Wisconsin dairy farm management strategies to improve: a) profitability, b) environmental stewardship, and c) long-term sustainability.</li>
      <li>And Objective 4, extension goal, heavily supported by Objective 3, is to promote enhanced dairy sustainability in Wisconsin through the dissemination and adoption of: a) lists of farm-specific best management practices and b) online decision support tool. </li>
    </ol>
