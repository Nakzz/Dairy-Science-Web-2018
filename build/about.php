  <h1>Short Bio</h1>
  <p>
      Dr. Victor E. Cabrera is an associate professor and extension specialist in dairy management
      at the University of Wisconsin-Madison Dairy Science Department. Dr. Cabrera combines applied
      research, interdisciplinary approaches, and participatory methods to deliver practical, user-friendly,
      and scholarly decision support tools for dairy farm management. These scientific tools are aimed
      to improve dairy farm profitability, environmental stewardship, and long-term sustainability of
      the dairy farm industry. During his short career, Dr. Cabrera has developed more than 40 decision
      support tools, published 54 refereed articles, and 5 book chapters, presented in more than 100
      scientific sessions, and given talks in more than 170 extension meetings in Wisconsin, other States,
      and several other countries. Dr. Cabrera work in the past 8 years has been pivotal to attract more
      than $4.0 million to support his research and extension initiatives. Dr. Cabrera has been
      distinguished with the University of Wisconsin-Madison Vilas Faculty Mid-Career Investigator Award,
      Second Mile Extension award of the Wisconsin Association of County Agricultural Agents, the Pound
      Extension Award and the Alfred Toepfer Faculty Fellow Award from the University of Wisconsin College of
      Agriculture and Life Sciences, the Distinguished Achievement Award from the University of Florida School
      of Natural Resources and Environment, and the Foundation Scholar Award in Dairy Production from the American
      Dairy Science Association.
    </p>


  <h1>Biography</h1>
  <p>Dr. Victor E. Cabrera was born and raised in the Inca's land of Cusco, Peru. After finishing High School,
    he moved to Lima to pursue his B.S. and Engineering degrees from La Molina Agrarian University.
     After graduation, he worked for three and a half years as an agriculture educator in a college.
     During that time he completed a 3-month post-graduate course on administration of agriculture schools in
     Spain and France. In 1997, Victor was awarded a fellowship from the Kellogg Foundation to pursue a M.S.
     degree in Farming Systems Research and Extension at the University of Florida. His M.S. research focused
     on improving small farm livelihood systems using alternative agronomic practices, which were evaluated with
     linear programming techniques. After finishing his M.S. degree, Victor accepted a 6-month interim position at
     the University of Florida Food and Resource Economics Department.
<br><br>
  Victor went back to Peru in 1999 where he worked as a consultant in a project of the Inter-American Development
    Bank for two years. In 2001, he returned to the U.S. to pursue his Ph.D. at the University of Florida School of
    Natural Resources working as a research and teaching assistant in the Food and Resource Economics Department.
    During his Ph.D. program, he became deeply involved with dairy industry and dairy farm systems developing a
    comprehensive decision support system to help dairy producers cope with increasing environmental restrictions
    while maintaining profitability. In 2004, right after graduating, Victor accepted a Postdoctoral position with
    the University of Miami, where he worked performing risk analyses of using climate information under government
    farm programs. In early 2006, Victor accepted the position of Assistant Professor and Extension Dairy Specialist
    with the New Mexico State University where he worked for two years. During that time, Dr. Cabrera worked performing
     applied research focused on environmental decision-making for the growing dairy industry in New Mexico and Western
      Texas.
  <br><br>
  In April 2008, he joined the faculty team of the Dairy Science Department at the University of Wisconsin-Madison
  as an Assistant Professor and Extension Specialist in Dairy Management with 70% Extension and 30% research appointment
  split. Victor was tenured and promote to Associate Professor in November 2013.
<br><br>
  Dr. Cabrera is focusing his extension and research programs on dairy farm economic decision-making for improved
  profitability and sustainability. Dr. Cabrera translates proven research into user-friendly decision making tools
  addressed to dairy producers, extension agents, and dairy farm consultants. During his short career, Victor has
  authored or coauthored >40 peer-reviewed publications, 5 book chapters, >50 Extension publications, >50 proceeding
  papers, and developed more than 40 scientific tools for decision-making.
  <br><br>
