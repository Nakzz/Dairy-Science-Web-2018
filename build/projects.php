              <p>This project will develop a "virtual dairy farm brain," a state-of-the-art suite of real-time integrated
                dairy farm management decision support tools. The "virtual dairy farm brain" will mimic actual farm management
                and will learn as it goes by applying complex machine learning pipelines and exploiting the interdependencies
                of the complex integrated biological, physical, and informational dimensions of dairy farm systems.
                The project will determine if a dairy farm can substantially improve its economic and environmental
                performance by interacting, adopting and applying integrated, databased analytics, expert systems, and
                artificial intelligence contained in whole farm decision support tools through the "virtual dairy farm brain."
                This innovative project is anticipated to transform how dairy farms will operate in the future and likely become the
                next big leap in dairy farm management. Dairy farms have embraced technological innovations and procured vast amounts
                of permanent data streams, but the problem is that they have not been able to integrate all this information to improve
                whole farm based management and decision-making. It is imperative to develop a system that can collect, integrate, manage,
                and analyze on-farm and off-farm data in real-time for practical and relevant actions. This project's main assets include
                using existing data - cow, herd, farm, market, weather, crops, and soils - and integrating these data streams to produce new
                knowledge and optimized decision-support tools.
                The project is especially important and relevant to Wisconsin, the largest dairy farm state (10,000 farms), in which dairying
                contributes half of the agricultural economy, has an impact of $43 billion a year, and supports 80,000 jobs. (<a href="../virtualdairyfarmbrain.php" target="_blank">More</a>)</p>
